c= 1            # Para o comando while fazer a mesma coisa
while c < 10:   # Temos que criar uma var de contagem no caso c
    print(c)    # Enquanto o c for menor que 10 se repetirá o laço
    c = c +1    # o contado c serve para cada vez que o laço passar por ele ele somata mais 1 a variavél c
print('FIM')    # somando de 1 em  1 na hora que atingir 10 encerrará o laço ( enquanto c<10)